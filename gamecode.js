var gameCode = 0;
var ctx = 0;

function gameInit() {
  var c = document.getElementById("gameArea");
  gameCode = document.getElementById("gameCodeArea");
  ctx = c.getContext("2d");
  setInterval(gameLoop, 10);
}

function gameLoop() {
  for (var i=0;i<mapHeight-1;i++) {
    for (var j=0;j<mapWidth-1;j++) {
      if (map[i][j] == 1) {
        ctx.beginPath();
        ctx.fillStyle = "black";
        ctx.rect(j * mapWidth, i * mapHeight, sqaureWidth, sqaureHeight);
        ctx.fill();
      }
    }
  }
  ctx.beginPath();
  ctx.fillStyle = "red";
  ctx.rect(playerX, playerY, sqaureWidth, sqaureHeight);
  ctx.fill();
}

function runCode() {
  eval(gameCode.value);
}

